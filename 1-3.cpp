#include<iostream>
using namespace std;
int main()
{
	cout << "length:char" << sizeof(char) << endl;
	cout << "length:short" << sizeof(short) << endl;
	cout << "length:int" << sizeof(int) << endl;
	cout << "length:long" << sizeof(long) << endl;
	cout << "length:float" << sizeof(float) << endl;
	cout << "length:double" << sizeof(double) << endl;
	cout << "length:bool" << sizeof(bool) << endl;
	cout << "length:long double" << sizeof(long double) << endl;
	return 0;
}